# Write your remainder function here:
def remainder(num1, num2):
  first_input = num1 * 2
  second_input = num2 / 2
  return (first_input % second_input)
# Uncomment these function calls to test your remainder function:
print(remainder(15, 14))
# should print 2
print(remainder(9, 6))
# should print 0